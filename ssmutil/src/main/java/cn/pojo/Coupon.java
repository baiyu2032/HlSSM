package cn.pojo;

public class Coupon {

  private long coid;
  private String coname;
  private long colo;
  private String one;
  private String two;
  private String three;


  public long getCoid() {
    return coid;
  }

  public void setCoid(long coid) {
    this.coid = coid;
  }


  public String getConame() {
    return coname;
  }

  public void setConame(String coname) {
    this.coname = coname;
  }


  public long getColo() {
    return colo;
  }

  public void setColo(long colo) {
    this.colo = colo;
  }


  public String getOne() {
    return one;
  }

  public void setOne(String one) {
    this.one = one;
  }


  public String getTwo() {
    return two;
  }

  public void setTwo(String two) {
    this.two = two;
  }


  public String getThree() {
    return three;
  }

  public void setThree(String three) {
    this.three = three;
  }

}
